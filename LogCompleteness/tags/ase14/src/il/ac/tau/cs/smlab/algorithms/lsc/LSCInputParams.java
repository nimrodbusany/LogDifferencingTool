package il.ac.tau.cs.smlab.algorithms.lsc;

import java.util.List;
import java.util.Map;

public class LSCInputParams {
	
	List<String> chart;
	boolean mineTriggers;
	Map<String, List<String>> charts;

	public LSCInputParams(List<String> chart, boolean mineTriggers) {
		super();
		this.chart = chart;
		this.mineTriggers = mineTriggers;
	}
	
	public LSCInputParams() {
		super();
		this.mineTriggers = false;
	}
	
	public LSCInputParams(Map<String, List<String>> charts) {
		super();
		this.mineTriggers = false;
		this.charts = charts;
	}


	public boolean isMineTriggers() {
		return mineTriggers;
	}


	public void setMineTriggers(boolean mineTriggers) {
		this.mineTriggers = mineTriggers;
	}


	public List<String> getChart() {
		return chart;
	}


	public void setChart(List<String> chart) {
		this.chart = chart;
	}


}
